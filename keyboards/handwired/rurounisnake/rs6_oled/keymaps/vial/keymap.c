bool process_record_user(uint16_t keycode, keyrecord_t *record) {
    switch (keycode) {
    case KC_DP:
        if (record->event.pressed) {
            inputSelected = 1;
        }
        break;

    case KC_HDMI1:
        if (record->event.pressed) {
            inputSelected = 2;
        }
        break;
    case KC_HDMI2:
        if (record->event.pressed) {
            inputSelected = 3;
        }
        break;
    case KC_VOLU:
        if (record->event.pressed) {
            inputSelected = 4;
			muted = false;
        }
        break;
    case KC_VOLD:
        if (record->event.pressed) {
            inputSelected = 5;
			muted = false;
        }
        break;
    case KC_MUTE:
        if (record->event.pressed) {
            inputSelected = 6;
			muted = !muted;
        }
        break;
    }
    return true;
};

